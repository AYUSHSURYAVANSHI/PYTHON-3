number = int(input("Enter the number for division :"))


if number % 2 == 0:
    print(" Number is Division by 2 : ")
elif number % 3 == 0:
    print(" Number is Division by 3 : ")
elif number % 5 == 0:
    print(" Number is Division by 5 : ")
elif number % 7 == 0:
    print(" Number is Division by 7 : ")
elif number % 11 == 0:
    print(" Number is Division by 11: ")
else :
    print("number is greater than 11 ")