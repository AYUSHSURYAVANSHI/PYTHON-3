# strings 
# collection of characters inside single quotes or double quotes 
# we can't add(+) two strings.
# but we can multiply two strings. 

first_name = " Ayush "
last_name = " Suryavanshi "
full_name = first_name + last_name 
print(full_name)
print(first_name + 3 ) # error
print(first_name + "3") # no error 
print(last_name + str(3)) # no error
print(first_name * 3 ) # no error
